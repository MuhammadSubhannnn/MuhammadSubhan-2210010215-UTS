    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String judul = txtJudul.getText().trim();
            String isiResep = isiResepArea.getText().trim();

            if (!judul.isEmpty() && !resepMap.containsKey(judul)) {
                listModel.addElement(judul);
                resepMap.put(judul, isiResep);
                JOptionPane.showMessageDialog(this, "Resep berhasil ditambahkan!");
            } else if (resepMap.containsKey(judul)) {
                JOptionPane.showMessageDialog(this, "Judul resep sudah ada.");
            } else {
                JOptionPane.showMessageDialog(this, "Judul resep tidak boleh kosong.");
            }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        String selected = listResep.getSelectedValue();
            if (selected != null) {
                listModel.removeElement(selected);
                resepMap.remove(selected);
                isiResepArea.setText("");
                JOptionPane.showMessageDialog(this, "Resep berhasil dihapus!");
            } else {
                JOptionPane.showMessageDialog(this, "Pilih resep untuk dihapus.");
            }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        String selected = listResep.getSelectedValue();
            String judulBaru = txtJudul.getText().trim();
            String isiBaru = isiResepArea.getText().trim();

            if (selected != null && !judulBaru.isEmpty()) {
                if (!resepMap.containsKey(judulBaru) || selected.equals(judulBaru)) {
                    // Update judul dan isi
                    resepMap.remove(selected);
                    listModel.set(listResep.getSelectedIndex(), judulBaru);
                    resepMap.put(judulBaru, isiBaru);
                    JOptionPane.showMessageDialog(this, "Resep berhasil diubah!");
                } else {
                    JOptionPane.showMessageDialog(this, "Judul baru sudah ada di daftar.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Pilih resep dan pastikan judul baru tidak kosong.");
            }
    }//GEN-LAST:event_btnUbahActionPerformed

